     void update()
    {
        NhanVien sp=getModel();
        try{
            dao.update(sp);
           
            load();
            DialogHelper.alert(this,"Cập nhật thành công ");
        }
        catch(Exception ex)
        {
           DialogHelper.alert(this,"Cập nhật thành công");
        }
    }

