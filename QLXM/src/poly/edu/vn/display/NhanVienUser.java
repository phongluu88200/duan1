    void load()
    { 
     NhanVien a= dao.findById(txtMaNV.getText());
       txtMaNV.setText(a.getId_NV());
        txtTenNV.setText(a.getTen_NV());
        txtPass.setText(a.getPass());
        txtSDT.setText(a.getSdt());
        txtEmail.setText(a.getEmail());
        txtDiaChi.setText(a.getDiaChi());
        txtNgaySinh.setText(DateHelper.toString(a.getNgaySinh()));
        lblHinh.setToolTipText(a.getHinh());
         try {
           
            BufferedImage hinh=ImageIO.read(new File(Paths.get("").toAbsolutePath().toString()+"/logos/"+a.getHinh()));
            
            
            lblHinh.setIcon(new ImageIcon(hinh));
        } catch (Exception ex) {
            System.err.println(ex);
        }
        boolean gt=a.getGioiTinh();
        if(gt==true)
        {
           rdbNam.setSelected(true);
        }
        
        if(gt==false)
        {
           rdbNu.setSelected(true);
        }
        
        boolean vt=a.getVaiTro();
        if(vt==true)
        {
          txtVaiTro.setText("Nhân viên");
        }
        
        if(vt==false)
        {
          txtVaiTro.setText("Nhân viên");
        }
    }
   NhanVien getModel()
    {
       
       String id_KH=txtMaNV.getText();
       String tenKH=txtTenNV.getText();
       String pass=txtPass.getText();
       String sdt=txtSDT.getText();
       String email=txtEmail.getText();
       String diaChi=txtDiaChi.getText();
       boolean gt;
       if(rdbNam.isSelected())  
       {
         gt=true;
       }
       else{
         gt=false;
       }
        Date ngaySinh=DateHelper.toDate(txtNgaySinh.getText());
         boolean vt=false;
       
        
       String hinh=lblHinh.getToolTipText();
       NhanVien model=new NhanVien(id_KH, tenKH, pass, sdt, email, diaChi, gt, ngaySinh, vt, hinh);
       return model;
    }
    
