    void edit()
    {
       try{
          String id=(String) tblGridView.getValueAt(this.index, 0);
         SanPham model= dao.findById(id);
         if(model!=null)
         {
            this.setModel(model);
            this.setStatus(false);
         }
       
       }
       catch(Exception ex)
       {
           DialogHelper.alert(this,"Lỗi truy vấn dữ liệu ");
       }
    }
    
  void find() {
        DefaultTableModel model = (DefaultTableModel) tblGridView.getModel();
        model.setRowCount(0);
        try {
            List<SanPham> list = dao.selectByKeyword(txtTim.getText());
            for (SanPham sp : list) {
                Object[] row = {
                        sp.getId_SP(),
                        sp.getTen_SP(),
                        sp.getId_NCC(),
                        sp.getId_loai(),
                        sp.getDonGia(),
                        sp.getSoLuong()
                };
                       
                model.addRow(row);
            }
        } 
        catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    
