    void load() {
        DefaultTableModel model = (DefaultTableModel) tblGridView.getModel();
        model.setRowCount(0);
        try {
            List<SanPham> list = dao.select();
            for (SanPham sp : list) {
                Object[] row = {
                        sp.getId_SP(),
                        sp.getTen_SP(),
                        sp.getId_NCC(),
                        sp.getId_loai(),
                        sp.getDonGia(),
                        sp.getSoLuong()
                };
                       
                model.addRow(row);
            }
        } 
        catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    
